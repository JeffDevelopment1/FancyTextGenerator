    public static string Convert(string input, string map)
    {
        var sb = new StringBuilder();

        foreach (var c in input)
        {
            int i = Normal.IndexOf(char.ToLower(c));
            sb.Append(i >= 0 ? map[i] : c);
        }

        return sb.ToString();
    }
